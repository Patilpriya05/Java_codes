public class LoopHelloWorld {
    public static void main(String args[]){

        int i = 0;
        //For Loop
        //for(i=0; i<10; i++)
        //   System.out.println("Hello World..");

        //While Loop
        while(i<10){
            System.out.println("\nHello World..");
        }

        //Do While Loop
        do{
            System.out.println("\nHello World..");
            i += 1;
        }while(i<10);
    }
}
